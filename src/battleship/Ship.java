package battleship;

public class Ship {
	
	int rowPosition;
	int columnPosition;
	
	public Ship(int rowPosition, int columnPosition) {
		super();
		this.rowPosition = rowPosition;
		this.columnPosition = columnPosition;
	}

	public int getRowPosition() {
		return rowPosition;
	}

	public int getColumnPosition() {
		return columnPosition;
	}
	
	
	
	

}
